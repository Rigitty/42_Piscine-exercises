int	ft_str_is_uppercase(char *str)
{
	int	i;
	int	r;

	i = 0;
	r = 1;
	while (str[i] != '\0')
	{
		if (!(str[i] >= 65 && str[i] <= 90))
		{
			r = 0;
			break ;
		}
		i++;
	}
	if (str[0] == '\0')
	{
		return (1);
	}
	return (r);
}
