int	ft_is_prime(int nb)
{
	int	r;
	int	i;

	r = 1;
	i = 2;
	if (nb < 2)
	{
		return (0);
	}
	if (nb == 2 || nb == 3)
	{
		return (r);
	}
	while (i <= nb / i)
	{
		if (nb % i == 0)
		{
			r = 0;
		}
		i++;
	}
	return (r);
}
